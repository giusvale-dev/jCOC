package org.jclash.domain;

public class League {

    private long id;
    private String name;
    private BadgeUrl iconUrls;

    public League() {

    }

    /**
     * @return the id
     */
    public long getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(long id) {
        this.id = id;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the iconUrls
     */
    public BadgeUrl getIconUrls() {
        return iconUrls;
    }

    /**
     * @param iconUrls the iconUrls to set
     */
    public void setIconUrls(BadgeUrl iconUrls) {
        this.iconUrls = iconUrls;
    }

    
    
}
