package org.jclash.domain;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;
import com.fasterxml.jackson.annotation.JsonProperty;

@JsonIgnoreProperties(ignoreUnknown = true)
public class Label {

    @JsonProperty
    private int id;

    @JsonProperty
    private String name;
    
    @JsonProperty
    private BadgeUrl iconUrls;

    public Label() {

    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the iconUrls
     */
    public BadgeUrl getIconUrls() {
        return iconUrls;
    }

    /**
     * @param iconUrls the iconUrls to set
     */
    public void setIconUrls(BadgeUrl iconUrls) {
        this.iconUrls = iconUrls;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + id;
        result = prime * result + ((name == null) ? 0 : name.hashCode());
        result = prime * result + ((iconUrls == null) ? 0 : iconUrls.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        Label other = (Label) obj;
        if (id != other.id)
            return false;
        if (name == null) {
            if (other.name != null)
                return false;
        } else if (!name.equals(other.name))
            return false;
        if (iconUrls == null) {
            if (other.iconUrls != null)
                return false;
        } else if (!iconUrls.equals(other.iconUrls))
            return false;
        return true;
    }
}
